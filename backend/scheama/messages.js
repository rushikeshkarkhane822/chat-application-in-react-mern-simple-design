const mongoose = require('mongoose');

const chatSchema = mongoose.Schema({
    message: {
        type: String,
        required: true
    },
    sender: {
        type: String,
        required: true
    },
    receiver: {
        type: String,
        required: true
    },
    time: {
        type: String,
        required: true
    },
    date: {
        type: String,
        required: true
    },
    
}, { timestamps: true });

module.exports = mongoose.model('Chat', chatSchema);
